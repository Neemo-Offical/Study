package src.Study.Study1;

    public class S2000501430_chapt1_1_9 {
        public static void main(String[] args) {
            double width=4.5;                                                           //声明宽
            double high=7.9;                                                            //声明高
            double area=width*high;                                                     //声明面积并定义运算
            double perimeter=2*(width+high);                                            //声明周长并定义运算
            System.out.print("area=width*high=4.5*7.9=");                               //在屏幕上显示面积的算式
            System.out.println(area);                                                   //输出结果
            System.out.print("perimeter=2*(width+high)=2*(4.5+7.9)=");                  //在屏幕上显示周长的算式
            System.out.println(perimeter);                                              //输出结果
    }
} 